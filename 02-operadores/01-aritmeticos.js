let a = 5
let b = 7

console.log(a + b, 'adicion') // adicion
console.log(a - b, 'resta') // resta
console.log(a * b, 'multiplicacion') // multiplicacion
console.log(a / b, 'división') // división
console.log(a % b, 'modulo (Lo que sobra') // modulo (Lo que sobra de una división)
console.log(a ** b, 'potencia') // potencia

// incrementar
console.log(++a) // primero suma y luego muestra
console.log(a++) // primero muestra y luego suma
console.log(a)

// decrementar
console.log(--a) // primero resta y luego muestra
console.log(a--) // primero muestra y luego resta
console.log(a)